'''
2)	Elaborar um programa que pergunte quatro valores inteiros e apresente 2 resultados:  a) Resultado de suas adições
b) Resultado de suas multiplicações
'''

a = int(input("Olá, por favor insira o primeiro valor:"))
b = int(input("Por favor insira o segundo valor:"))
c = int(input("Por favor insira o terceiro valor:"))
d = int(input("Por favor insira o quarto valor:"))

soma = a + b+ c + d
print("Soma dos quatro valores:", soma)

mut = a * b * c * d

print("Multiplicação dos quatro valores:", mut)