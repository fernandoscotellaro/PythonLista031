'''
7)	Fazer um algoritmo que pergunte 3 números e apresente a média aritmética entre estes 3 números
'''

a = int(input("Olá, insira o primeiro número:"))
b = int(input("Insira aqui o segundo número:"))
c = int(input("Insira aqui o terceiro número:"))

media = (a + b + c) / 3

print("A média entre esses números é:", media)